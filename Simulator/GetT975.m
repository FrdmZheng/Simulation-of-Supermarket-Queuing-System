function t = GetT975(n, data)
    if (1 <= n) && (n <=30)
        t = data(data(:, 2) == n, 1);
    elseif (30 < n) && (n <= 40)
        t = 2.0420 - (2.0420 - 2.0210) * (n - 30) / 10;
    elseif (40 < n) && (n <= 50)
        t = 2.0210 - (2.0210 - 2.0090) * (n - 40) / 10;
    elseif (50 < n) && (n <= 75)
        t = 2.0090 - (2.0090 - 1.9920) * (n - 50) / 25;
    elseif (75 < n) && (n <= 100)
        t = 1.9920 - (1.9920 - 1.9840) * (n - 75) / 25;
    else
        t = 1.9600;
    end
end